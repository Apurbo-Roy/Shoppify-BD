    public void knapsack(int n, double weight[], double profit[], double capacity) {
        double x[] = new double[100];
        double tp = 0, u;
        int i, j;
        u = capacity;

        for (i = 0; i < n; i++) {
            x[i] = 0.0;
        }

        for (i = 0; i < n; i++) {
            if (weight[i] > u) {
                break;
            } else {
                x[i] = 1.0;
                tp = tp + profit[i];
                u = u - weight[i];
            }
        }

        if (i < n) {
            x[i] = u / weight[i];
        }

        tp = tp + (x[i] * profit[i]);

        jLabel8.setText(String.valueOf(df.format(tp)));

        for (i = 0; i < n; i++) {

            String st = String.valueOf(df.format(x[i]*100));
            model.insertRow(tblData.getRowCount(), new Object[]{
                "Product " + String.valueOf(i + 1), st+"%"
            });

        }

        WrongInput.setVisible(true);
        WrongInput1.setVisible(true);
        jPanel1.setVisible(true);

    }

